package da02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello students\nToday we have Java class");
        System.out.println("------------------------------------");
        System.out.println("\tJava programming language");
        System.out.println("\t\tJava Developer Course Batch 1");
        System.out.println("----------------------------------");
        System.out.println("\\");
        System.out.println("-----------------------------");
        System.out.println("My favorite TV show is \"The Peaky Blinders\"");

    }
}
/*
   1. \n : a new line
   2. \t : a paragraph space
   3. \\ : back slash
   4. \" : double quote
 */