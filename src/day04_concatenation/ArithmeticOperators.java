package day04_concatenation;

public class ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(100+200);   // 300   // addition operator "+"
        System.out.println(40-10); // 30  // substraction operator "-"
        System.out.println(" ---------");
        System.out.println(50 / 6);  // 8 integer #
        System.out.println(50 / 6.0);  // 8.333.. double #
        System.out.println(50.0 / 6);  // 8.333..  double #
        System.out.println(50d / 6);   // 8.333.. "d" stands for double
        System.out.println(2.5 / 0.5);   // 5.0 is double #
        System.out.println( (int ) (2.5 / 0.5) );   // 5 is for integer int

        System.out.println("-----------------------");

        System.out.println(10 *2);
        System.out.println(5*5);

        System.out.println("----");
        // reminder %  - modulus
        System.out.println(100 % 30); // 10   // 100 / 30 = 30.3333
        System.out.println(45 % 8);  // 5








    }
}
