package day04_concatenation;

public class Concatenation_Addition {
    public static void main(String[] args) {
        System.out.println("32"+1); // 321
        System.out.println(7+"1");  // 71
        System.out.println("13"+"2");  // 132
        System.out.println("------------");
        // Example addition
        System.out.println(12+13);  // 25
        System.out.println("--------------");
        // example
        System.out.println("The value is "+ true); // The value is true




    }
}
